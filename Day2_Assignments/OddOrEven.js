//Create a function to check the number is odd or even
function isOddOrEven(varvalue) {
    if (varvalue % 2 == 0) {
        console.log("Number " + varvalue + " is a even number")
    }
    else {
        console.log("Number " + varvalue + " is a odd number")
    }
}


let varvalue = 49
isOddOrEven(varvalue)
